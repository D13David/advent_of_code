import arrays

const width = 5
const height = 7

fn run(data[] string) {
    mut keys := []int{}
    mut locks := []int{}
    mut result := 0

    mut offset := 0
    for offset < data.len {
        mut counts := []int{len: width, init: 0}
        for y in offset..offset + height {
            for x in 0 .. width {
                if data[y][x] == `#` {
                    counts[x]++
                }
            }
        }

        if data[offset] == '#####' {
            keys = arrays.append(keys, counts)
        } else {
            locks = arrays.append(locks, counts)
        }

        offset += (height + 1)
    }

    mut i := 0
    for i < keys.len {
        mut j := 0
        for j < locks.len {
            mut match_found := true
            for k in 0 .. width {
                match_found = match_found && (keys[i+k] + locks[j+k] <= height)
            }

            if match_found {
                result++
            }

            j += width
        }

        i += width
    }

    println(result)
}

