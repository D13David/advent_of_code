typedef struct node_t
{
    int index;
    int dist;
    struct node_t* next;
} node;

#define MAX_NODES 970
void* allocBatch;
struct node_t* allocFreeList;

void MM_free(struct node_t* node);

void MM_init()
{
    allocBatch = malloc(MAX_NODES * sizeof(struct node_t));

    struct node_t* node = (struct node_t*)allocBatch;
    for (int i = 0; i < MAX_NODES; ++i)
    {
        MM_free(node++);
    }
}

node* MM_alloc()
{
    if (!allocFreeList) {
        printf("out of memory...\n");
        exit(0);
    }

    struct node_t* node = allocFreeList;
    allocFreeList = *((struct node_t**)allocFreeList);
    return node;
}

void MM_free(struct node_t* node)
{
    *(struct node_t**)node = allocFreeList;
    allocFreeList = node;
}

node* allocNode(int dist, int index)
{
    struct node_t* node = MM_alloc();
    node->index = index;
    node->dist = dist;
    node->next = NULL;
    return node;
}

bool empty(struct node_t** head)
{
    return (*head) == NULL;
}

int top(struct node_t** head)
{
    return (*head)->index;
}

void pop(struct node_t** head)
{
    struct node_t* node = *head;
    (*head) = (*head)->next;
    MM_free(node);
}

void push(struct node_t** head, int dist, int index)
{
    struct node_t* newNode = allocNode(dist, index);

    if (*head == NULL)
    {
        (*head) = newNode;
        return;
    }

    if ((*head)->dist > dist)
    {
        newNode->next = *head;
        (*head) = newNode;
    }
    else
    {
        struct node_t* currentHead = *head;

        while (currentHead->next != NULL && currentHead->next->dist < dist)
        {
            currentHead = currentHead->next;
        }

        newNode->next = currentHead->next;
        currentHead->next = newNode;
    }
}

int findShortestPath(int startIndex, int endIndex)
{
    unsigned int* distance = (unsigned int*)malloc(NUM_ELEMENTS * sizeof(unsigned int));
    assert(distance != NULL && "allocation failed");
    for (int i = 0; i < NUM_ELEMENTS; ++i)
        distance[i] = 0xffffffff;

    struct node_t* queue = NULL;
    push(&queue, 0, startIndex);
    distance[startIndex] = 0;

    while (!empty(&queue))
    {
        int index = top(&queue);
        pop(&queue);

        for (int i = 0; i < 4; ++i)
        {
            int neighbour = adjacency[index][i];
            if (neighbour == -1)
                continue;

            unsigned int pathCost = data[neighbour] + distance[index];
            if (distance[neighbour] > pathCost)
            {
                distance[neighbour] = pathCost;
                push(&queue, pathCost, neighbour);
            }
        }
    }

    int shortestPath = distance[endIndex];
    free(distance);
    return shortestPath;
}

